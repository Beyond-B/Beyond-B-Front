package com.example.beyond_b.diary;

public class DiaryDetail {
    private int diaryId;
    private String date;
    private String event;
    private String thought;
    private String emotionSpecific;
    private String behavior;
    private String result;
    private String feeling;


    public int getDiaryId() {
        return diaryId;
    }

    public void setDiaryId(int diaryId) {
        this.diaryId = diaryId;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getEvent() {
        return event;
    }

    public void setEvent(String event) {
        this.event = event;
    }

    public String getThought() {
        return thought;
    }

    public void setThought(String thought) {
        this.thought = thought;
    }

    public String getEmotionSpecific() {
        return emotionSpecific;
    }

    public void setEmotionSpecific(String emotionSpecific) {
        this.emotionSpecific = emotionSpecific;
    }

    public String getBehavior() {
        return behavior;
    }

    public void setBehavior(String behavior) {
        this.behavior = behavior;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getFeeling() {
        return feeling;
    }

    public void setFeeling(String feeling) {
        this.feeling = feeling;
    }
}
